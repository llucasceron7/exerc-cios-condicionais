let dia = 3;

if (dia <= 0 || dia >= 8) {
  console.log("Dia da semana inválido!");
} else if (dia == 1){
  console.log("Domingo");
} else if (dia == 2){
  console.log("Segunda");
} else if (dia == 3){
  console.log("Terça");
} else if (dia == 4){
  console.log("Quarta");
} else if (dia == 5){
  console.log("Quinta");
} else if (dia == 6){
  console.log("Sexta");
} else if (dia == 7){
  console.log("Sábado");
}